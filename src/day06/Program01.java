package day06;

public class Program01 {

	public static void main(String[] args) {
		// + - * /(Q) %(R)
		int a = 20;
		int b = 3;
		double c = 3;
		//int () int -->int
		//int () double/float ---> double/float
		
		System.out.println(a+b);
		System.out.println(a+c);
		System.out.println(b+c);
		
		System.out.println("");
		
		System.out.println(a-b);
		System.out.println(a-c);
		System.out.println(b-c);
		
		System.out.println("");
		
		System.out.println(a*b);
		System.out.println(a*c);
		System.out.println(b*c);
		
		System.out.println("");
		
		System.out.println(a/b);
		System.out.println(a/c);
		System.out.println(b/c);
		
		System.out.println("");
		
		System.out.println(a%b);
		System.out.println(a%c);
		System.out.println(b%c);

	}

}
